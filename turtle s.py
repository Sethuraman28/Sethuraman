import turtle as t
t.hideturtle()
t.color("cyan")
t.begin_fill()
t.speed(1)
t.bgcolor("black")
t.pensize(1)
t.penup()
t.goto(100,0)
t.pendown()
t.fd(-100)
t.circle(-100,-180)
t.rt(180)
t.circle(-100,180)
t.fd(100)
t.bgcolor("black")
t.pensize(1)
t.penup() #outer s shape
t.goto(-100,-400)
t.pendown()
t.circle(-5,180)
t.fd(90)
t.circle(90,180)
t.rt(180)
t.circle(110,-180)
t.fd(-110)
t.circle(5,-181)

t.end_fill()
t.done()