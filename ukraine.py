import turtle as t
t.hideturtle()
t.color("yellow")
t.begin_fill()
t.fd(200)
t.lt(90)
t.fd(100)
t.lt(90)
t.fd(400)
t.lt(90)
t.fd(100)
t.lt(90)
t.fd(200)
t.end_fill()
t.penup()
t.goto(-200,100)
t.pendown()
t.color("blue")
t.begin_fill()
t.fd(400)
t.lt(90)
t.fd(100)
t.lt(90)
t.fd(400)
t.lt(90)
t.fd(100)
t.end_fill()
t.done()

